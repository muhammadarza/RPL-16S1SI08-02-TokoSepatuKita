public class Kasir {
    private int idKasir;
    private String namaKasir;
    private int tlpKasir;
    private String almtKasir;

    public int getIdKasir() {
        return idKasir;
    }

    public void setIdKasir(int idKasir) {
        this.idKasir = idKasir;
    }

    public String getNamaKasir() {
        return namaKasir;
    }

    public void setNamaKasir(String namaKasir) {
        this.namaKasir = namaKasir;
    }

    public int getTlpKasir() {
        return tlpKasir;
    }

    public void setTlpKasir(int tlpKasir) {
        this.tlpKasir = tlpKasir;
    }

    public String getAlmtKasir() {
        return almtKasir;
    }

    public void setAlmtKasir(String almtKasir) {
        this.almtKasir = almtKasir;
    }
}
