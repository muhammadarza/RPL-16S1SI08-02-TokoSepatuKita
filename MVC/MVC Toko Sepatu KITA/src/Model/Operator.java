public class Operator {
    private int idOperator;
    private String nmOperator;
    private int tlpOperator;
    private String almtOperator;

    public int getIdOperator() {
        return idOperator;
    }

    public void setIdOperator(int idOperator) {
        this.idOperator = idOperator;
    }

    public String getNmOperator() {
        return nmOperator;
    }

    public void setNmOperator(String nmOperator) {
        this.nmOperator = nmOperator;
    }

    public int getTlpOperator() {
        return tlpOperator;
    }

    public void setTlpOperator(int tlpOperator) {
        this.tlpOperator = tlpOperator;
    }

    public String getAlmtOperator() {
        return almtOperator;
    }

    public void setAlmtOperator(String almtOperator) {
        this.almtOperator = almtOperator;
    }
}
