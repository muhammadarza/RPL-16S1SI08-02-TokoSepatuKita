package model_TokoSepatuKita;

/**
 *
 * @author Arief
 */
public class Karyawan {
    private int idKaryawan;
    private String namaKaryawan;
    private String tglBergabung;
    private int tlpKaryawan;

    public int getIdKaryawan() {
        return idKaryawan;
    }

    public void setIdKaryawan(int idKaryawan) {
        this.idKaryawan = idKaryawan;
    }

    public String getNamaKaryawan() {
        return namaKaryawan;
    }

    public void setNamaKaryawan(String namaKaryawan) {
        this.namaKaryawan = namaKaryawan;
    }

    public String getTglBergabung() {
        return tglBergabung;
    }

    public void setTglBergabung(String tglBergabung) {
        this.tglBergabung = tglBergabung;
    }

    public int getTlpKaryawan() {
        return tlpKaryawan;
    }

    public void setTlpKaryawan(int tlpKaryawan) {
        this.tlpKaryawan = tlpKaryawan;
    }
    
    
}
