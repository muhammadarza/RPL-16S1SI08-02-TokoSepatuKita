    private class Pelanggan(){
        private $Id_Pelanggan;
        private $Nm_Pelanggan;
        private $Alamat_Pelanggan;
        private $Telp_Pelanggan;
    }
    
        function setId_Pelanggan(){
            $this->Id_Pelanggan=$Id_Pelanggan;
        }
        function getId_Pelanggan(){
            return $this->Id_Pelanggan;
        }
        
        function setNm_Pelanggan(){
            $this->Nm_Pelanggan=$Nm_Pelanggan;
        }
        function getNm_Pelanggan(){
            return $this->Nm_Pelanggan;
        }
        
        function setAlamat_Pelanggan(){
            $this->Alamat_Pelanggan=$Alamat_Pelanggan;
        }
        function getAlamat_Pelanggan(){
            return $this->Alamat_Pelanggan;
        }
        
        function setTelp_Pelanggan(){
            $this->Telp_Pelanggan=$Telp_Pelanggan;
        }
        function getTelp_Pelanggan(){
            return $this->Telp_Pelanggan;
        }