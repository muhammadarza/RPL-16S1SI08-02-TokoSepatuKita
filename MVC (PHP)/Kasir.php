            $this->Nm_Kasir=$Nm_Kasir;
        }
        function getNm_Kasir(){
            return $this->Nm_Kasir;
        }
        
        function setAlamat_Kasir(){
            $this->Alamat_Kasir=$Alamat_Kasir;
        }
        function getAlamat_Kasir(){
            return $this->Alamat_Kasir;
        }
        
        function setTelp_Kasir(){
            $this->Telp_Kasir=$Telp_Kasir;
        }
        function getTelp_Kasir(){
